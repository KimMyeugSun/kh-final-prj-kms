CREATE TABLE IF NOT EXISTS club_board_category
(
    no bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 9223372036854775807 CACHE 1 ),
    allowed character(1) COLLATE pg_catalog."default",
    create_at timestamp(6) without time zone,
    delete_at timestamp(6) without time zone,
    name character varying(50) COLLATE pg_catalog."default" NULL,
    purpose character varying(1000) COLLATE pg_catalog."default",
    update_at timestamp(6) without time zone,
    eno bigint NULL,
    CONSTRAINT club_board_category_pkey PRIMARY KEY (no),
    CONSTRAINT club_board_category_allowed_check CHECK (allowed = ANY (ARRAY['Y'::bpchar, 'N'::bpchar, 'D'::bpchar]))
)

TABLESPACE pg_default;
ALTER TABLE IF EXISTS club_board_category OWNER to postgres;

CREATE TABLE IF NOT EXISTS club_board
(
    no bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 9223372036854775807 CACHE 1 ),
    content character varying(3000) COLLATE pg_catalog."default" NOT NULL,
    create_at timestamp(6) without time zone,
    hit integer NOT NULL,
    status character varying(2) COLLATE pg_catalog."default",
    title character varying(100) COLLATE pg_catalog."default" NOT NULL,
    update_at timestamp(6) without time zone,
    cno bigint NOT NULL,
    eno bigint NOT NULL,
    CONSTRAINT club_board_pkey PRIMARY KEY (no),
    CONSTRAINT fk_club_board_category_no FOREIGN KEY (cno)
        REFERENCES public.club_board_category (no) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT fk_employee_no_to_club_board FOREIGN KEY (eno)
        REFERENCES public.employee (eno) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT club_board_status_check CHECK (status::text = ANY (ARRAY['NE'::character varying, 'DA'::character varying, 'WD'::character varying]::text[]))
)

TABLESPACE pg_default;
ALTER TABLE IF EXISTS club_board OWNER to postgres;

